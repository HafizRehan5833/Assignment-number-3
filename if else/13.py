#13. Take two numbers and an operator (+, -, *, /) as input and perform the corresponding operation.
a=int(input("Enter value:"))
#d=input("Enter q for quit:")
b=int(input("Enter value:"))
c=input("Enter what operation--> ")
#d=0
if c=="+":
    print(a+b)
elif c == "-":
    print(a-b)
elif c=="/":
    print(a/b)    
else:
    print(a*b)
 

